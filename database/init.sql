CREATE TABLE IF NOT EXISTS dashboard_settings (
    id INT AUTO_INCREMENT PRIMARY KEY,
    dashboard_name VARCHAR(50) CHARACTER SET utf8,
    team_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Team',
    name_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Name',
    presence_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Status',
    note1_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Note 1',
    note2_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Note 2',
    note3_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Note 3',
    check1_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Check 1',
    check2_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Check 2',
    check3_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Check 3',
    updated_at_label VARCHAR(50) CHARACTER SET utf8 DEFAULT 'Last Updated',
    hide_note1 BOOLEAN DEFAULT FALSE,
    hide_note2 BOOLEAN DEFAULT FALSE,
    hide_note3 BOOLEAN DEFAULT FALSE,
    hide_check1 BOOLEAN DEFAULT FALSE,
    hide_check2 BOOLEAN DEFAULT FALSE,
    hide_check3 BOOLEAN DEFAULT FALSE,
    hide_updated_at BOOLEAN DEFAULT FALSE,
    team_width INT DEFAULT 120,
    name_width INT DEFAULT 100,
    presence_width INT DEFAULT 100,
    note1_width INT DEFAULT 100,
    note2_width INT DEFAULT 100,
    note3_width INT DEFAULT 100,
    check1_width INT DEFAULT 80,
    check2_width INT DEFAULT 80,
    check3_width INT DEFAULT 80,
    updated_at_width INT DEFAULT 100,
    grid_width INT DEFAULT 460,
    grid_height INT DEFAULT 460,
    notes TEXT CHARACTER SET utf8
);

CREATE TABLE IF NOT EXISTS users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    team VARCHAR(50) CHARACTER SET utf8,
    name VARCHAR(50) CHARACTER SET utf8 NOT NULL,
    presence VARCHAR(10) CHARACTER SET utf8 NOT NULL,
    note1 VARCHAR(50) CHARACTER SET utf8,
    note2 VARCHAR(50) CHARACTER SET utf8,
    note3 VARCHAR(50) CHARACTER SET utf8,
    check1 BOOLEAN DEFAULT FALSE,
    check2 BOOLEAN DEFAULT FALSE,
    check3 BOOLEAN DEFAULT FALSE,
    `order` INT NOT NULL,
    dashboard_id INT,
    x INT,
    y INT,
    width INT DEFAULT 80,
    height INT DEFAULT 40,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);


